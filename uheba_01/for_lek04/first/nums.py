def plus(a, b):
    return a + b

def minus(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def modulo(a, b):
    return a % b

if __name__ == '__main__':
    print(plus(1, 2))
    print(minus(1, 2))
    print(multiply(1, 2))
    print(divide(1, 2))
    print(modulo(1, 2))
